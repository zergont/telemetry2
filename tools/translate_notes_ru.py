def main():
    entries = []
    with open(MAP_FILE, encoding='utf-8') as f:
        for line in f:
            line = line.strip()
            if line:
                entries.append(json.loads(line))

    filled = 0
    skipped_nonempty = 0
    not_in_dict = 0

    for e in entries:
        addr = e['addr']
        if e.get('notes_ru', '').strip():
            skipped_nonempty += 1
            continue
        if addr in TRANSLATIONS:
            e['notes_ru'] = TRANSLATIONS[addr]
            filled += 1
        else:
            not_in_dict += 1

    with open(MAP_FILE, 'w', encoding='utf-8', newline='\n') as f:
        for e in entries:
            f.write(json.dumps(e, ensure_ascii=False) + '\n')

    print(f"Заполнено notes_ru : {filled}")
    print(f"Уже было непустых  : {skipped_nonempty}")
    print(f"Нет в словаре      : {not_in_dict}")

    # Проверка: что осталось пустым
    entries2 = [json.loads(l) for l in open(MAP_FILE, encoding='utf-8') if l.strip()]
    still_empty = [e for e in entries2 if not e.get('notes_ru', '').strip()]
    print(f"\nВсё ещё пустых notes_ru: {len(still_empty)}")
    if still_empty:
        print("Адреса:")
        for e in still_empty:
            print(f"  {e['addr']:6d}  {e.get('name','')}")


if __name__ == '__main__':
    main()
